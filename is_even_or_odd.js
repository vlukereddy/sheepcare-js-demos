function isOdd(number){
  
  if(number % 2 !== 0) {
    console.log("The number is odd.");
  } else {
    console.log("The number is even.");
  }  
}


function isEven(number){
   if(number % 2 === 0){
     console.log("The number is even.");
   } else {
     console.log("The number is odd");
   }
}


isOdd(3);
isEven(3);
